#ifndef POINTS___H
#define POINTS___H

// A point with x and y both being integers.
typedef struct Point2i {
	int x;
	int y;
} Point2i;

// A point with x and y both being floats.
typedef struct Point2f {
	float x;
	float y;
} Point2f;

// A point with x and y both being doubles.
typedef struct Point2d {
	double x;
	double y;
} Point2d;

#endif
